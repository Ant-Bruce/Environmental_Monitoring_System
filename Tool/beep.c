/****************************************************************************
*函数名：Beep_warn
*输  入：value:烟雾浓度数值
*输  出：无
*功  能：蜂鸣器报警
****************************************************************************/
void Beep_warn(uint value)
{
	uchar i;
	if(value>=20||((wen_dat[13]-'0')*10+(wen_dat[14]-'0'))>=25)
	{
		for(i=0;i<255;i++)
		{   
			beep=~beep;
			Delay_beep(5);
		}
	}
}